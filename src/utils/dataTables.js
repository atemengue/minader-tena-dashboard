export const getBadge = (position) => {
  switch (position) {
    case 1:
      return "success";
    case 2:
    case 14:
    case 13:
    case 18:
      return "danger";
    case 7:
    case 9:
      return "warning";
    case 6:
      return "primary";
    default:
      return "primary";
  }
};

